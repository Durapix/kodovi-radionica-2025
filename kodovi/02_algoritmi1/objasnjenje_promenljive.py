print ("b =", b)
print()
# Пример 8: Логичким променљивама можемо да вршимо логичке операције,
# али такође можемо у њима да памтимо резултате неких провера.
# У овом примеру, памтимо да ли је 5 веће од 3 (јесте).

print ("Пример 9: Редослед операција")
a = 5
b = a < 10 or a > 13 # тачно, 5 < 10 ИЛИ 5 > 13
a = a + 2 == 6 # нетачно, 7 није једнако 6
print ("a =", a)
print ("b =", b)
print()
