# Можемо од рачунара затражити да се нека локација у меморији резервише.
# Тој локацији у меморији ми дајемо име, и дефинишемо вредност коју ће имати.
# Када у програму напишемо име те променљиве, програм користи ту меморијску локацију.
# Пајтон не третира типове података као трајне, и промениће тип податка који променљива
