# Пример 2: Ми у било ком тренутку можемо тој променљивој да задамо неку нову вредност.
# Код који се изврши након те промене ће користити ову нову вредност.

print ("Пример 3 - Сабирање и одузимање:")
a = a + 2
b = a - 2
print ("a =", a)
print ("b =", b)
print()

# Пример 3: Можемо да променимо вредности променљивих користећи њу саму
# или друге променљиве, као и операторе и функције.
# О операторима и функцијама ћемо више причати другде.
# НАПОМЕНА: Када користимо исту променљиву са обе стране једнакости, не читамо је исто
# НАПОМЕНА: Са леве стране = читамо је као нову вредност променљиве
# НАПОМЕНА: Са десне стране = читамо је као стару вредност променљиве
# a = a + 2 нам заправо значи "нова вредност променљиве а нам је старо а + 2"

print ("Пример 4: Множење и степеновање")
a = b * 3
b = b ** 3
print ("a =", a)
print ("b =", b)
print()

# Пример 4: Оператор * нам значи множење када га употребимо једном, а степеновање када
# га удвостручимо у оператор **

print ("Пример 5: Целобројно дељење и остатак")
a = b // 3
b = b % 3
print ("a =", a)
print ("b =", b)
print()

# Пример 5: Оператор // нам значи целобројно дељење
# Оператор % нам значи остатак при дељењу

print ("Пример 6: Реални бројеви и њихово дељење")
