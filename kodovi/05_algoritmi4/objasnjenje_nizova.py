# У Пајтону, низови су један од начина чувања више података у једној променљивој
# Представљају једну од основних алатки за рад и већини програмских језика
# У Пајтону се ретко користе директно. У овом примеру ћемо све време користити
# нешто што се зове "листа", али користићемо је као да користимо низ.
# Постоје разлике међу њима које ћемо касније објаснити. Низ је једноставнији.
# Низ (или листу) правимо тако што неку вредност ставимо у угласте заграде []

Niz = []
print (Niz)
print ()

# Као и са скуповима, вредности се уписују са зарезом између

Niz = [1, False, "бла", True, 1]
print (Niz)
print ()

# Приметите да нам се овај пут вредности исписују тачно оним редоследом којим смо их
# уписали. То је зато што су низови уређен тип податка, и редослед је важан.
# То што је редослед важан можемо употребити да приступимо тачно одређеном члану низа.
# Члану низа се приступа преко угластих заграда [], и броје се од нуле.

print ("Члан низа са редним бројем 0:", Niz[0])
print ("Члан низа са редним бројем 1:", Niz[1])
print ("Члан низа са редним бројем 2:", Niz[2])
print ("Члан низа са редним бројем 3:", Niz[3])
print ("Члан низа са редним бројем 4:", Niz[4])
print()

# Пошто нам редослед значи нешто, није никакав проблем што нам елемент 0 и елемент 4
# имају исту вредност 1. Ако имати списак за куповину, и прво на списку вам је млеко
# кога купујете једну литру, а последње на списку вам је брашно, кога купујете један
# килограм, то су два различита податка, и немају везе један с другим.

