# IndexError: list index out of range
# Тај изузетак значи да сте покушали да приступите непостојећем члану низа
# Низу од 5 чланова је последњи члан са редним бројем 4

# Као и са скуповима, дужину низа добијамо користећи функцију len()
print ("Дужина низа Niz:", len(Niz))
print()

# Елементе низа можемо да променимо. Као што елементу приступамо преко редног броја
# да би га прочитали, тако му приступамо преко редног броја да би у њега писали
Niz[0]="нова вредност"
Niz[3]=True
print (Niz)
print ()

# Niz[5]=0
# Ако извршите ред кода у коментару изнад, десиће се изузетак
# IndexError: list assignment index out of range
# Тај изузетак значи да сте покушали да измените непостојећи члан низа

# Ако желимо да приступимо само делу низа, то радимо преко оператора :
# Ако желимо да узмемо прва два елемента низа, то ћемо написати овако [:2]
print ("Прва два елемента:", Niz[:2])
# Обратите пажњу да су нам исписани елементи 0 и 1, али не и елемент 2
print ()

# Ако желимо да приступимо свим елементима од елемента бро 3, то нам је [3:]
print ("Елементи почевши од редног броја 3:", Niz[3:])
# Када од њега крећемо, елемент 3 нам је исписан
print ()

# Ако желимо елементе од редног броја 1 до редног броја 3, то нам је [1:4]
# Приметите да користимо 4, а не 3. Да смо хтели и 4 да испишемо, писали би 5
print ("Елементи почевши од редног броја 1 до редног броја 3:", Niz[1:4])
print ()

# Листу и низ можемо помножити са неким бројем да направимо већу листу
# која се понавља
print ("Испод исписујем наш низ 2 пута")
veci_niz = Niz * 2
print (veci_niz)
